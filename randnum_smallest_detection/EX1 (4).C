/*
*********************************************************************************************************
*                                             CREATE TASKS
*********************************************************************************************************
*/

static  void  TaskStartCreateTasks (void)
{
    INT8U  i;


    for (i = 0; i < N_TASKS; i++) {                        /* Create N_TASKS identical tasks           */
        TaskData[i] = '0' + i;                             /* Each task will display its own letter    */
        OSTaskCreate(Task, (void *)&TaskData[i], &TaskStk[i][TASK_STK_SIZE - 1], i + 1);
    }
}

/*
*********************************************************************************************************
*                                                  TASKS
*********************************************************************************************************
*/

INT8U rd_search(INT8U* OSRdyTbl, INT8U* OSRdyGrp, INT8U* arr);
void  Task(void* pdata)
{

    INT8U OSRdyGrp;
    INT8U OSRdyTbl[8];
    INT8U  err;

    int x = 0;
    int y = 0;
    int color = 0;
    int final = 64;
    INT8U temp = 64;
    int col = 0;

    for (;;) {

        int i = 0;
        INT8U rnum[4] = { -1, -1, -1, -1 };

        INT8U OSRdyGrp = 0x00;

        memset(OSRdyTbl, 0x00, sizeof(OSRdyTbl));



        for (; i < 4; i++) {
            rnum[i] = rd_search(OSRdyTbl, &OSRdyGrp, rnum);

            PC_DispChar(x++, y + 5, '0' + (rnum[i] / 10), DISP_FGND_BLACK + DISP_BGND_LIGHT_GRAY);

            PC_DispChar(x++, y + 5, '0' + (rnum[i] % 10), DISP_FGND_BLACK + DISP_BGND_LIGHT_GRAY);
            x++;
        }

        temp = (OSUnMapTbl[OSRdyGrp] << 3) + OSUnMapTbl[OSRdyTbl[OSUnMapTbl[OSRdyGrp]]];

        if (temp < final && rnum[0] != NULL) {
            switch (color)
            {
            case 0:
                PC_DispChar(x++, y + 5, '0' + (temp / 10), DISP_FGND_WHITE + DISP_BGND_RED);

                PC_DispChar(x++, y + 5, '0' + (temp % 10), DISP_FGND_WHITE + DISP_BGND_RED);
                break;
            case 1:
                PC_DispChar(x++, y + 5, '0' + (temp / 10), DISP_FGND_WHITE + DISP_BGND_BLUE);

                PC_DispChar(x++, y + 5, '0' + (temp % 10), DISP_FGND_WHITE + DISP_BGND_BLUE);
                break;
            case 2:
                PC_DispChar(x++, y + 5, '0' + (temp / 10), DISP_FGND_WHITE + DISP_BGND_GREEN);

                PC_DispChar(x++, y + 5, '0' + (temp % 10), DISP_FGND_WHITE + DISP_BGND_GREEN);
                break;
            case 3:
                PC_DispChar(x++, y + 5, '0' + (temp / 10), DISP_FGND_WHITE + DISP_BGND_BROWN);

                PC_DispChar(x++, y + 5, '0' + (temp % 10), DISP_FGND_WHITE + DISP_BGND_BROWN);
                break;
            }
            final = temp;

            color = (color + 1) % 4;
        }
        if (final == 0) final = 64;


        y++;

        if (y > 15) {
            y = 0;
            col += 15;
        }
        x = col;
        OSTimeDly(200);
    }


}


INT8U rd_search(INT8U* OSRdyTbl, INT8U* OSRdyGrp, INT8U* arr) {

    INT8U n;

    n = random(64);




    while (1) {
        int f = 0;
        int t = 0;
        for (; t < 4; t++) {
            if (arr[t] == n) {
                f = 1;
            }
        }

        if (f == 0) {
            *OSRdyGrp |= OSMapTbl[n >> 3];
            OSRdyTbl[n >> 3] |= OSMapTbl[n & 0x07];
            break;
        }
        else {

            n = random(64);


        }
    }
    return n;
}





